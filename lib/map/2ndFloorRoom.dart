    SPA_2room1,
    SPA_2room2,
    SPA_2room3,
    SPA_2room4,
    GRADE10YELLOW_2room1,
    GRADE10YELLOW_2room2,
    G10Building4F_2Room1,
    G10Building4F_2Room2,
    G10Building4F_2Room3,
    G10Building4F_2Room4,
    STEA_2Room1,
    STEA_2Room2,
    STEB_2R1,
    STEB_2R2,
    STEB_2R3,
    STEC_2R1,
    STEC_2R2,
    STEC_2R3,
    STEC_2R4,
    STEC_2R5
  ];

  // Polygons with similar properties
  final Color SecondLayerColor = const Color.fromARGB(255, 245, 196, 175);
  final Color SecondLayerBorderColor = const Color.fromARGB(255, 161, 116, 97);
  final double borderStrokeWidth = 1.0;

  final List<Polygon> SecondLayerPolygons = SecondLayerPoints.map((points) =>
      createPolygon(points, SecondLayerColor, borderStrokeWidth,
          SecondLayerBorderColor)).toList();

  // Define the polygons with other similar properties in lists
