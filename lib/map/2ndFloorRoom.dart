  Polygon createPolygon(List<LatLng> points, Color color,
      double borderStrokeWidth, Color borderColor) {
    return Polygon(
      points: points,
      color: color,
      borderStrokeWidth: borderStrokeWidth,
      borderColor: borderColor,
    );
  }

