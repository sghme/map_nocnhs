  final List<Polygon> whitePolygons = whitePolygonPoints
      .map((points) => createPolygon(points, Colors.white.withOpacity(0.3),
          borderStrokeWidth, Colors.white))
      .toList();

  // Combine all polygons in the final layer
  return PolygonLayer(
    polygons: [
      ...SecondLayerPolygons,
      ...greyPolygons,
      ...whitePolygons,
    ],
  );
}
